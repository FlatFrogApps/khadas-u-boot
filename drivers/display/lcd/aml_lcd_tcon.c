#define PR_BUF_MAX    100
void lcd_tcon_regs_table_print(struct tcon_config_s *tconf)
{
	int i, j, n;
	char *buf;

	if (tconf == NULL)
		return;
	if (tconf->tcon_enable == 0) {
		LCDERR("%s: invalid tcon\n", __func__);
		return;
	}

	buf = (char *)malloc(PR_BUF_MAX * sizeof(char));
	if (buf == NULL) {
		LCDERR("%s: buf malloc error\n", __func__);
		return;
	}

	LCDPR("%s:\n", __func__);
	for (i = 0; i < 256; i++) {
		n = snprintf(buf, PR_BUF_MAX, "0x%03x: ", (i * 16));
		for (j = 0; j < 16; j++) {
			n += snprintf(buf+n, PR_BUF_MAX, " 0x%02x",
					tconf->reg_table[i*16+j]);
		}
		buf[n] = '\0';
		printf("%s\n", buf);
	}
	free(buf);
}

void lcd_tcon_regs_readback_print(void)
{
	int i, j, n;
	char *buf;

	buf = (char *)malloc(PR_BUF_MAX * sizeof(char));
	if (buf == NULL) {
		LCDERR("%s: buf malloc error\n", __func__);
		return;
	}

	LCDPR("%s:\n", __func__);
	for (i = 0; i < 256; i++) {
		n = snprintf(buf, PR_BUF_MAX, "0x%03x: ", (i * 16));
		for (j = 0; j < 16; j++) {
			n += snprintf(buf+n, PR_BUF_MAX, " 0x%02x",
					lcd_tcon_read(i*16+j));
		}
		buf[n] = '\0';
		printf("%s\n", buf);
	}
	free(buf);
}

int lcd_tcon_regs_update(unsigned char *table, int len)
{
	int i;

	if (len > LCD_TCON_TABLE_MAX) {
		LCDERR("%s: invalid table len %d\n", __func__, len);
		return -1;
	}

	LCDPR("%s:", __func__);

	for (i = 0; i < len; i++)
		lcd_tcon_write(i, table[i]);

	LCDPR("%s: finished\n", __func__);

	return 0;
}

static void lcd_tcon_top_output_config(struct tcon_config_s *tconf)
{
	/* tcon sync signals alignment to do */
	lcd_tcon_write(0x1002, 0x45603012);
	lcd_tcon_write(0x1003, 0x0);
}

static void lcd_tcon_top_axi_offset(unsigned int addr)
{
	if (addr == 0) {
		LCDERR("%s: invalid address: 0x%08x\n", __func__, addr);
		return;
	}
	lcd_tcon_write(0x100c, addr);
	LCDPR("set tcon fb_addr: 0x%08x\n", addr);
}

static int lcd_tcon_top_config(struct tcon_config_s *tconf)
{
	LCDPR("%s\n", __func__);

	lcd_tcon_write(0x100e, 0x001f);
	lcd_tcon_write(0x1000, 0x0199);
	lcd_tcon_write(0x1009, 0x0037);
	lcd_tcon_write(0x100b, 0x003f);
	lcd_tcon_write(0x100b, 0x0000);

	return 0;
}

int lcd_tcon_init(struct tcon_config_s *tconf)
{
	if (tconf == NULL)
		return -1;
	if (tconf->tcon_enable == 0) {
		LCDERR("%s: invalid tcon\n", __func__);
		return -1;
	}

	lcd_tcon_top_axi_offset(tconf->fb_addr);

	lcd_tcon_top_config(tconf);
	lcd_tcon_regs_update(tconf->reg_table, LCD_TCON_TABLE_MAX);

	lcd_tcon_top_output_config(tconf);

	return 0;
}

void lcd_tcon_disable(void)
{
	LCDPR("%s\n", __func__);
	lcd_tcon_setb(0x1000, 0, 4, 1);
}

